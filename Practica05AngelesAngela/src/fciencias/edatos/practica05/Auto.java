package fciencias.edatos.practica05;

public class Auto {
    public String marca;
    public String modelo;
    public String placas;
    public String anio;
    public String nombre;
    public String materno;
    public String paterno;
    public String opinion;

    public Auto(String marca, String modelo, String placas, String anio, String nombre, String materno, String paterno, String opinion){
        this.marca = marca;
        this.modelo = modelo;
        this.nombre = nombre;
        this.materno = materno;
        this.paterno = paterno;
        this.placas = placas;
        this.anio = anio;
        this.opinion = opinion;
    }
}
